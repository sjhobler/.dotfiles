  init = function()
    -- LSP Keybindings (only active when LSP attaches)
    vim.api.nvim_create_autocmd('LspAttach', {
      group = vim.api.nvim_create_augroup('SnacksLspKeymaps', { clear = true }),
      callback = function(ev)
        if vim.bo[ev.buf].filetype == 'oil' then
          return
        end

        local function map(keys, func, desc, mode)
          mode = mode or 'n'
          vim.keymap.set(mode, keys, func, { buffer = ev.buf, desc = desc, silent = true })
        end

        -- LSP Navigation
        map('gd', function()
          Snacks.picker.lsp_definitions()
        end, 'Goto Definition')
        map('gD', function()
          Snacks.picker.lsp_declarations()
        end, 'Goto Declaration')
        map('gr', function()
          Snacks.picker.lsp_references()
        end, 'References')
        map('gI', function()
          Snacks.picker.lsp_implementations()
        end, 'Goto Implementation')
        map('gy', function()
          Snacks.picker.lsp_type_definitions()
        end, 'Goto T[y]pe')
        map('gai', function()
          Snacks.picker.lsp_incoming_calls()
        end, 'C[a]lls Incoming')
        map('gao', function()
          Snacks.picker.lsp_outgoing_calls()
        end, 'C[a]lls Outgoing')

        -- LSP Symbols
        map('<leader>ss', function()
          Snacks.picker.lsp_symbols()
        end, 'LSP Symbols')
        map('<leader>sS', function()
          Snacks.picker.lsp_workspace_symbols()
        end, 'LSP Workspace Symbols')
        -- Other useful LSP keybindings
        --
        map('<leader>ca', vim.lsp.buf.code_action, 'Code Action', { 'n', 'v' })
        map('<leader>rn', vim.lsp.buf.rename, 'Smart Rename')
        map('K', vim.lsp.buf.hover, 'Hover Documentation')
        -- map('<leader>d', vim.diagnostic.open_float, 'Line Diagnostics')
        map('[d', function()
          vim.diagnostic.jump { count = -1, float = true }
        end, 'Previous Diagnostic')
        map(']d', function()
          vim.diagnostic.jump { count = 1, float = true }
        end, 'Next Diagnostic')
        map('<leader>rs', '<cmd>LspRestart<CR>', 'Restart LSP')
      end,
    })

    -- other Autocmds
    vim.api.nvim_create_autocmd('User', {
      pattern = 'VeryLazy',
      callback = function()
        -- Setup some globals for debugging (lazy-loaded)
        _G.dd = function(...)
          Snacks.debug.inspect(...)
        end
        _G.bt = function()
          Snacks.debug.backtrace()
        end
        vim.print = _G.dd -- Override print to use snacks for `:=` command

        -- Create some toggle mappings
        Snacks.toggle.option('spell', { name = 'Spelling' }):map '<leader>us'
        Snacks.toggle.option('wrap', { name = 'Wrap' }):map '<leader>uw'
        Snacks.toggle.option('relativenumber', { name = 'Relative Number' }):map '<leader>uL'
        Snacks.toggle.diagnostics():map '<leader>ud'
        Snacks.toggle.line_number():map '<leader>ul'
        Snacks.toggle.option('conceallevel', { off = 0, on = vim.o.conceallevel > 0 and vim.o.conceallevel or 2 }):map '<leader>uc'
        Snacks.toggle.treesitter():map '<leader>uT'
        Snacks.toggle.option('background', { off = 'light', on = 'dark', name = 'Dark Background' }):map '<leader>ub'
        Snacks.toggle.inlay_hints():map '<leader>uh'
      end,
    })
  end,
